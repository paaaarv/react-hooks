// useEffect: persistent state
// http://localhost:3000/isolated/exercise/02.js

import * as React from 'react'

function useLocalStorageState(key, defaultValue = ''){
  const [attribute, setAttribute] = React.useState(() => window.localStorage.getItem(key) || defaultValue)

  React.useEffect(() => {
    window.localStorage.setItem(key, attribute)
  }, [key, attribute])

  return [attribute, setAttribute]
}

function Greeting({initialName = ''}) {
  // ğŸ¨ initialize the state to the value from localStorage
  // ğŸ’° window.localStorage.getItem('name') ?? initialName
  

  // ğŸ¨ Here's where you'll use `React.useEffect`.
  // The callback should set the `name` in localStorage.
  // ğŸ’° window.localStorage.setItem('name', name)

  const [name, setName] = useLocalStorageState('name', initialName)
  function handleChange(event) {
    setName(event.target.value)
  }
  return (
    <div>
      <form>
        <label htmlFor="name">Name: </label>
        <input value={name} onChange={handleChange} id="name" />
      </form>
      {name ? <strong>Hello {name}</strong> : 'Please type your name'}
    </div>
  )
}

function App() {
  return <Greeting  />
}

export default App
